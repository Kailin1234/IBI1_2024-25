a = 0
for i in range(1,11):
    a = a+i
    print(a)

# Learned from partner in practical.
# the original number is 0.
# every time it enters the for loop, it can be updated by adding i value.
# every time it gets out the loop, it will output a result value.
# then it can enter the loop again, adding the next i in the range, until all the i values are added.